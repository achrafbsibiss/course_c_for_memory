#include <stdio.h>

int main(void){
  // constant variable
  // constant variable is a variable that cannot be changed
  // constant variable is declared with the const keyword
  // constant variable is declared with a name in all caps
  // useing good practice to name constant variables in all caps
  const int DAYS_IN_WEEK = 7;
  printf("days in week is %d\n", DAYS_IN_WEEK);
  const float PI= 3.14;
  return 0;


};